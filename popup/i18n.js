  /**
   * 检测浏览器语言，返回最匹配的支持语言代码
   */
  detectLanguage() {
    const browserLangs = navigator.languages || [navigator.language];
    for (const lang of browserLangs) {
      // 精确匹配
      if (this.messages[lang]) return lang;
      // 匹配基础语言 (如 zh-CN -> zh-CN, zh-TW -> zh-TW)
      const base = lang.split('-')[0];
      // 特殊处理中文
      if (base === 'zh') {
        if (lang.includes('TW') || lang.includes('HK') || lang.includes('Hant')) return 'zh-TW';
        return 'zh-CN';
      }
      // 特殊处理葡萄牙语
      if (base === 'pt') return 'pt-BR';
      // 尝试基础语言
      if (this.messages[base]) return base;
    }
    return 'en'; // 默认英文
  },

  /**
   * 获取翻译文本
   */
  t(key) {
    const locale = this.currentLocale || 'en';
    const msgs = this.messages[locale] || this.messages['en'];
    return msgs[key] || this.messages['en'][key] || key;
  },

  currentLocale: 'en',
